#include "3-calc.h"

/**
 * op_add - calc the sum of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: Sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Calc the difference of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: Difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Calc the product of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: Product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Calc the division of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: Division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Calc the remainder of the division of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
